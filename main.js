    this.isEditMode = false;
    this.editorWrap.addClass("brax-hidden");
    this.previewPane.removeClass("brax-hidden");
    this.closeSearch();
    if (this.toggleAction) {
      this.toggleAction.empty();
      this.toggleAction.setAttr("aria-label", t(this.plugin.settings, "edit"));
      (0, import_obsidian.setIcon)(this.toggleAction, "pencil");
    }
    this.renderApp(this.editor.value);
